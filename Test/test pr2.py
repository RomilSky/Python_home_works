import math


def square(side):
    # Вычисляем площадь квадрата
    area = side ** 2

    # Если сторона не целое число, округляем результат вверх
    if not isinstance(side, int):
        area = math.ceil(area)

    return area


# Примеры использования:
print(square(5))  # Вывод: 25 (целое число, округление не требуется)
print(square(3.2))  # Вывод: 11 (не целое число, результат округлен вверх)
print(square(4.7))  # Вывод: 23 (не целое число, результат округлен вверх)